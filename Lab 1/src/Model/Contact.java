package Model;

/**
 *
 * @author saketh
 */
public class Contact {
    String phone;
    String email;

    
    public Contact(){
        this.email = "";
        this.phone = "";
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
}


